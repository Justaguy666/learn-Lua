-- if - then - else

-- An if statement tests its condition and executes its then-part or its else-part accordingly.
-- The else-part is optional.

-- The syntax is:

-- if condition then do() then

-- if condition then doA() else doB() end

--[[
    if condition then
        doA()
    else
        doB()
    end
]]

